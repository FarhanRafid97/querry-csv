const DropdownMenuSubTrigger = React.forwardRef<
  React.ElementRef<typeof DropdownMenuPrimitive.SubTrigger>,
  React.ComponentPropsWithoutRef<typeof DropdownMenuPrimitive.SubTrigger> & {
    inset?: boolean;
    showArrow?: boolean;
    arrowSide?: 'left' | 'right';
  }
>(({ className, inset, children, showArrow = true, arrowSide = 'right', ...props }, ref) => (
  <DropdownMenuPrimitive.SubTrigger
    ref={ref}
    className={cn(
      ' flex cursor-default  select-none items-center rounded-sm px-2 py-1.5  text-sm outline-none focus:bg-accent data-[state=open]:bg-accent w-full ',
      inset && 'pl-8',
      className
    )}
    {...props}
  >
    {showArrow && arrowSide === 'left' && (
      <div className="flex items-center justify-start h-2 w-4 mr-auto">
        {/* You can flip the icon or swap to a left-pointing one */}
        <TriangleRightIcon className="h-4 w-4 text-accent-foreground/60 rotate-180" />
      </div>
    )}
    <div className="text-[13px] whitespace-nowrap font-normal flex items-center gap-2 capitalize text-foreground ">
      {children}
    </div>
    {showArrow && arrowSide === 'right' && (
      <div className="flex items-center justify-end h-2 w-4 ml-auto">
        <TriangleRightIcon className="h-4 w-4 text-accent-foreground/60" />
      </div>
    )}
  </DropdownMenuPrimitive.SubTrigger>
));
DropdownMenuSubTrigger.displayName = DropdownMenuPrimitive.SubTrigger.displayName;

